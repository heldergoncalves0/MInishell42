int	ft_isalpha(int str)
{
	return ((str >= 'a' && str <= 'z') || (str >= 'A' && str <= 'Z'));
}

int	ft_isspace(char s)
{
	if (s == ' ' || (s > 8 && s < 14))
		return (1);
	return (0);
}
