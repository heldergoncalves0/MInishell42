#include "minishell.h"

void	pwd_cmd(char **env)
{
	int	i;

	i = 0;
	while (env[i] && ft_strncmp(env[i], "PWD=", 4))
		i++;
	if (env[i])
		printf("%s\n", env[i] + 4);
}
